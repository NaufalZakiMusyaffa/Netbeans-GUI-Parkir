    private void btnSignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignupActionPerformed
        // TODO add your handling code here:
        if (txtUsername.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Semua harus diisi");
        txtUsername.setText("");
        txtPassword.setText("");
        txtFullname.setText("");
        return;
        } else if (txtPassword.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Semua harus diisi");
        txtUsername.setText("");
        txtPassword.setText("");
        txtFullname.setText("");
        return;
       } else if (txtFullname.getText().equals("")) {
       JOptionPane.showMessageDialog(null, "Semua harus diisi");
        txtUsername.setText("");
        txtPassword.setText("");
        txtFullname.setText("");
        return;
       } else {
        JOptionPane.showMessageDialog(null, "Register Berhasil");
        username.add(txtUsername.getText());
        password.add(String.valueOf(txtPassword.getPassword()));
        txtUsername.setText("");
        txtPassword.setText("");
        txtFullname.setText("");
       }

    }//GEN-LAST:event_btnSignupActionPerformed

