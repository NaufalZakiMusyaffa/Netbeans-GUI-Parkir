    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:
        Long waktumasuk = Long.parseLong(jtMasuk.getText());
        Long waktukeluar = Long.parseLong(jtkeluar.getText());
        Long totalwaktu = waktukeluar - waktumasuk;
        Long mobil = new Long(5000);
        Long motor = new Long(2000);
        String totalwaktustr = String.valueOf(totalwaktu);
        if (totalwaktustr.contains("-")) {
        jtlama.setText("");
        txt_bayar.setText("");
        } else {
        jtlama.setText(totalwaktustr);    
        }
        if (lieur.getText().contains("Motor")) {
        Long totalbayar = totalwaktu * motor;
        String totalbayarstr = String.valueOf(totalbayar);
        txt_bayar.setText(totalbayarstr);    
        } else if (lieur.getText().equals("Mobil")) {
        Long totalbayar1 = totalwaktu * mobil;
        String totalbayar1str = String.valueOf(totalbayar1);
        txt_bayar.setText(totalbayar1str);
        } else {
            
        }

       

    }//GEN-LAST:event_btnHitungActionPerformed

    private void cekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cekActionPerformed
        // TODO add your handling code here:
        Long uangmasuk = Long.parseLong(uangm.getText());
        Long uangbayar = Long.parseLong(txt_bayar.getText());
        Long totaluang = uangmasuk - uangbayar;
        String totaluangstr = String.valueOf(totaluang);        
        if (uangm.getText().equals(txt_bayar.getText())) {
        JOptionPane.showMessageDialog(null, "TerimaKasih,Pembayaran anda berhasil");
        kmbl.setText("0");
        } else if (totaluangstr.contains("-")) {
        JOptionPane.showMessageDialog(null, "Maaf, uang anda kurang");
        kmbl.setText("");
        uangm.setText("");
        } else if (txt_bayar.getText().equals("")) {
        JOptionPane.showMessageDialog(null, "Klik hitung terlebih dahulu");
        uangm.setText("");
        return; 
        } else {JOptionPane.showMessageDialog(null, "Terimakasih telah membayar");    
        kmbl.setText(totaluangstr);    
        }
            
    }//GEN-LAST:event_cekActionPerformed

